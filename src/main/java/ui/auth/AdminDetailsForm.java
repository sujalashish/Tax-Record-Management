    private void btnUploadPhotoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUploadPhotoActionPerformed

    JFileChooser chooser = new JFileChooser();

    int result = chooser.showOpenDialog(this);

    if (result == JFileChooser.APPROVE_OPTION) {

        File file = chooser.getSelectedFile();
photoPath = file.getAbsolutePath();
        String path = file.getAbsolutePath();

        ImageIcon icon = new ImageIcon(path);

        Image img = icon.getImage().getScaledInstance(
                lblPhoto.getWidth(),
                lblPhoto.getHeight(),
                Image.SCALE_SMOOTH
        );
        lblPhoto.setText("");
        lblPhoto.setIcon(new ImageIcon(img));

    }

   // TODO add your handling code here:
    }//GEN-LAST:event_btnUploadPhotoActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
JOptionPane.showMessageDialog(this, "Details Saved Successfully!");

Dashboard dashboard = new Dashboard();
dashboard.setVisible(true);

this.dispose(); // closes current form

    // PERSONAL INFO

    admin.setDob(txtDOB.getText());

    admin.setGender(
            cmbGender.getSelectedItem().toString()
    );


if(rbYes.isSelected()) {

    admin.setMaritalStatus("Yes");

} else {

    admin.setMaritalStatus("No");
}


    // CONTACT INFO

    admin.setMobile(txtMobile.getText());

    admin.setAlternateMobile(
            txtMobileAlternate.getText()
    );

    admin.setAddress1(txtAddress1.getText());

    admin.setAddress2(txtAddress2.getText());

    admin.setCity(txtCity.getText());

    admin.setState(
            cmbState.getSelectedItem().toString()
    );

    admin.setPinCode(txtPinCode.getText());



    // DOCUMENTS

    admin.setAadhar(txtAadhar.getText());

    admin.setPan(txtPanCard.getText());



    // ACCOUNT

    admin.setAccountStatus(
            cmbAccountStatus.getSelectedItem().toString()
    );

    admin.setRole(
            cmbRole.getSelectedItem().toString()
    );



    // PHOTO

    admin.setPhotoPath(photoPath);



    // SAVE TO DATABASE

    AdminDAO dao = new AdminDAO();

    boolean saved = dao.registerAdmin(admin);



    if(saved) {

        JOptionPane.showMessageDialog(
                this,
                "Registration Successful"
        );

    } else {

        JOptionPane.showMessageDialog(
                this,
                "Registration Failed"
        );
    }
// TODO add your handling code here:

    }//GEN-LAST:event_btnSaveActionPerformed

