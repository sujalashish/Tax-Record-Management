    private void txtPanCardKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPanCardKeyTyped
 String text = txtPanCard.getText();

    char c = evt.getKeyChar();

    if (text.length() >= 10) {
        evt.consume();
        return;
    }

    if (text.length() < 5) {

        if (!Character.isLetter(c)) {
            evt.consume();
        }

        evt.setKeyChar(
                Character.toUpperCase(c)
        );

    } else if (text.length() < 9) {

        if (!Character.isDigit(c)) {
            evt.consume();
        }

    } else {

        if (!Character.isLetter(c)) {
            evt.consume();
        }

        evt.setKeyChar(
                Character.toUpperCase(c)
        );
    }        // TODO add your handling code here:
    }//GEN-LAST:event_txtPanCardKeyTyped

